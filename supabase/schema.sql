-- Run this in Supabase SQL Editor.

begin;

create extension if not exists pgcrypto;

create table if not exists public.valves (
  id bigint generated by default as identity primary key,
  valve_id text not null unique,
  customer text,
  cell text,
  size text,
  status text not null,
  test_type text,
  valve_type text,
  bowl_type text,
  order_type text,
  due_date date,
  date_tested date,
  date_closed date,
  description text,
  notes text,
  is_turnaround boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.priority_queue (
  id bigint generated by default as identity primary key,
  valve_id text not null unique,
  created_at timestamptz not null default now()
);

create table if not exists public.test_logs (
  id bigint generated by default as identity primary key,
  tested_on date not null,
  valve_id text not null,
  size text,
  pressure text,
  manufacturer text,
  valve_type text,
  test_type text,
  worked text,
  pass_fail text,
  action_taken text,
  tester text,
  created_at timestamptz not null default now()
);

-- Canonical customer names for dropdowns (e.g. New job). valves.customer still stores the name as text.
create table if not exists public.customers (
  id bigint generated by default as identity primary key,
  name text not null,
  created_at timestamptz not null default now(),
  constraint customers_name_unique unique (name)
);

create index if not exists idx_customers_name on public.customers(name);

-- Dropdown options for New job (admin-managed; app falls back to constants if empty).
create table if not exists public.lookup_values (
  id bigint generated by default as identity primary key,
  category text not null,
  value text not null,
  sort_order int not null default 0,
  created_at timestamptz not null default now(),
  constraint lookup_values_category_value_key unique (category, value)
);

create index if not exists idx_lookup_values_cat_sort on public.lookup_values(category, sort_order, id);

-- Files / photos for jobs (paths live in Storage bucket valve-attachments).
create table if not exists public.valve_attachments (
  id bigint generated by default as identity primary key,
  valve_row_id bigint not null references public.valves(id) on delete cascade,
  storage_path text not null,
  file_name text not null,
  mime_type text,
  kind text not null default 'file',
  created_at timestamptz not null default now(),
  constraint valve_attachments_kind_chk check (kind in ('photo', 'file')),
  constraint valve_attachments_storage_path_key unique (storage_path)
);

create index if not exists idx_valve_attachments_valve on public.valve_attachments(valve_row_id);

-- Inspection & Test Plan (ITP) — one document per job card (valves.id).
create table if not exists public.valve_itp (
  id bigint generated by default as identity primary key,
  valve_row_id bigint not null references public.valves(id) on delete cascade,
  content text not null default '',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint valve_itp_valve_unique unique (valve_row_id)
);

create index if not exists idx_valve_itp_valve on public.valve_itp(valve_row_id);

alter table public.valve_itp add column if not exists itp_data jsonb not null default '{}'::jsonb;

-- Shop procedures / work instructions by valve type (Resources page).
create table if not exists public.valve_type_procedures (
  id bigint generated by default as identity primary key,
  valve_type text not null,
  title text not null,
  body text not null default '',
  sort_order int not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_valve_type_procedures_vt_sort
  on public.valve_type_procedures (valve_type, sort_order, id);

