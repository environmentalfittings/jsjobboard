create index if not exists idx_itp_templates_job_valve_sort
  on public.itp_templates (job_type, valve_type, step_order, id);

create table if not exists public.job_itp_items (
  id bigint generated by default as identity primary key,
  valve_row_id bigint not null references public.valves(id) on delete cascade,
  template_step_id bigint references public.itp_templates(id) on delete set null,
  step_name text not null,
  required boolean not null default true,
  is_checked boolean not null default false,
  sort_order int not null default 0,
