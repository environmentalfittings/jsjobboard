    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'priority_queue'
  ) then
    alter publication supabase_realtime add table public.priority_queue;
  end if;

  if not exists (
    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'test_logs'
  ) then
    alter publication supabase_realtime add table public.test_logs;
  end if;

  if not exists (
    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'technicians'
  ) then
    alter publication supabase_realtime add table public.technicians;
  end if;
end
$$;

