create table if not exists public.job_technicians (
  id bigint generated by default as identity primary key,
  valve_row_id bigint not null references public.valves(id) on delete cascade,
  technician_id bigint not null references public.technicians(id) on delete cascade,
  created_at timestamptz not null default now(),
  unique (valve_row_id, technician_id)
);

create index if not exists idx_job_technicians_valve on public.job_technicians(valve_row_id);
create index if not exists idx_job_technicians_technician on public.job_technicians(technician_id);

insert into public.job_technicians (valve_row_id, technician_id)
select v.id, (elem.value)::bigint
from public.valves v
cross join lateral jsonb_array_elements_text(coalesce(v.assigned_technician_ids, '[]'::jsonb)) as elem(value)
where (elem.value)::bigint > 0
on conflict (valve_row_id, technician_id) do nothing;

