do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'technicians'
      and column_name = 'team'
  ) then
    update public.technicians
    set group_team = coalesce(group_team, team);
  end if;
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'technicians'
      and column_name = 'is_active'
  ) then
    update public.technicians
    set active = is_active
    where active is distinct from is_active;
  end if;
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'technicians'
      and column_name = 'work_cell_specialties'
      and udt_name = 'text'
  ) then
    alter table public.technicians
      alter column work_cell_specialties type text[]
      using case
        when work_cell_specialties is null or btrim(work_cell_specialties) = '' then '{}'::text[]
        else array_remove(regexp_split_to_array(work_cell_specialties, '\s*,\s*'), '')
      end;
  end if;
end
$$;

